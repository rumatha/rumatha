#===================================================================================================

class Segment:
    """
    Segment.
    """

    #-----------------------------------------------------------------------------------------------

    def __init__(self, lo, hi):
        """
        Constructor.

        Parameters
        ----------
        lo : float
            Lo value.
        hi : float
            Hi value.
        """

        if lo > hi:
            raise Exception('geom1d:Segment.__init__: unable to create segment with lo > hi.')

        self.lo = lo
        self.hi = hi

#===================================================================================================

if __name__ == '__main__':
    pass

